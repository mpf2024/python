package HanJia.运算;
            /*
            + ，-， *，:同小学一样
                   /(除法): JAVA中，整数除于整数只能得到整数。如果想得到小数，必须带有小数参与运算。
                   %(取模）:获取的是两个数据做除法的余数
             */
public class 算数运算符9 {
                public static void main(String[] args) {
                    System.out.println(5/2);
                    System.out.println(5.0/2);
                    System.out.println(5/2.0);
                    System.out.println(5.0/2.0);
                    System.out.println("——————————————————");
                    System.out.println(5%2);
                    System.out.println(4%2);


                }
}
