package HanJia.运算;
/*
                十进制：JAVA中   数值默认都是十进制，不需要加任何修饰。
                二进制：数值前面0b 开头，b大小写都可以
                八进制：数值前面0开头
                十六进制：数值前面0x开头，x大小写都可以
 */
public class 不同进制的书写格式3 {
    public static void main(String[] args) {
        System.out.println(154);
        System.out.println(0b1010);
        System.out.println(013);
        System.out.println(0x5a);
    }
}
