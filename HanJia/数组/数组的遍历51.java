package HanJia.数组;

public class 数组的遍历51 {
        /*
              数组的遍历：
                      将数组中所有数据取出来，打印在控制台
              数组中的属性：数组名.length
         */
        public static void main(String[] args) {
            int []arr={11,22,33,44,55,33,44,55};
            for(int i=0;i<arr.length;i++){
                System.out.println(arr[i]);
            }
            //快捷键  arr.fori+回车键
            for (int i = 0; i < arr.length; i++) {
                System.out.println(arr.length);
            }
        }
}
