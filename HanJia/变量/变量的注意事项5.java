package HanJia.变量;
/*


         变量的注意事项：
                     1.变量名不允许重复使用
                     2.一条语句可以定义多个变量，中间需要使用逗号隔开
                     3.变量在使用前，一定要赋值


 */
public class 变量的注意事项5 {
    public static void main(String[] args) {
        int num=10;
        // int num=20;  变量名不允许重复
        int a=10,b=20,c=30,d=40;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
